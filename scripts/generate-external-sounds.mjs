import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const soundsDirectory = path.join(root, "ui/modules/apps/TaxiDriverHUD/sounds");
const output = path.join(root, "ui/modules/apps/TaxiDriverHUD/external/sounds-data.js");
const files = (await fs.readdir(soundsDirectory))
  .filter((file) => file.toLowerCase().endsWith(".mp3"))
  .sort();
const sounds = {};
for (const file of files) {
  const bytes = await fs.readFile(path.join(soundsDirectory, file));
  sounds[file] = `data:audio/mpeg;base64,${bytes.toString("base64")}`;
}
const content = `// Generated by scripts/generate-external-sounds.mjs.\nwindow.TaxiDriverSoundData = ${JSON.stringify(sounds)};\n`;
await fs.writeFile(output, content, "utf8");
console.log(`Embedded ${files.length} TaxiDriver sounds.`);
